package ex04_operators;

public class Lab033_operators_arithmetic {
    public static void main(String[] args) {
        int a=90;
        int b=3;

        //addition
        System.out.println(a+b);
        //subtraction
        System.out.println(a-b);
        //multiplication
        System.out.println(a*b);
        //division
        System.out.println(a/b);
        //reminder
        System.out.println(a%b);

    }
}
